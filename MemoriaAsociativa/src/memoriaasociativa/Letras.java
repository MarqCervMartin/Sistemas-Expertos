    public void pintar(JPanel jPanel, int indice){
        if(count[indice] > 0 ){
            jPanel.setBackground(Color.white);
            count[indice]--;
            //System.out.println(count[0]);
        }else{
            jPanel.setBackground(Color.black);
            count[indice]++;
            //System.out.println(count[0]);
        }
    }
